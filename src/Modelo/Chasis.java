package Modelo;

/**
 *
 * @author Estudiante
 */
public class Chasis {
    private int x;
    private int y;
    private int ancho;
    private int alto;

    public Chasis(int x, int y, int ancho, int alto) {
        this.x = x;
        this.y = y;
        this.ancho = ancho;
        this.alto = alto;
    }
    
    public void mover(){
        this.x++;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getAncho() {
        return ancho;
    }

    public int getAlto() {
        return alto;
    }
    
}
